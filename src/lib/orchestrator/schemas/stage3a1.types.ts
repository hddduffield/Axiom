// ────────────────────────────────────────────────────────────────────────
// Post-fill: deterministic transform from the narrower LLM output to the
// full Stage3a1ResponseBody shape. The harness calls this after parsing
// the LLM response, then re-validates the result against the full schema.
//
// `recIdToFilePath` carries the per-rec source file paths that the harness
// resolved during KB load (Phase 1 step 1.1). Any rec_id missing from the
// map gets an empty source_file_path; downstream tools may flag this, but
// the schema accepts any non-throwing string.
// ────────────────────────────────────────────────────────────────────────

export function harnessPostFillFields(
  raw: Stage3a1LlmRawOutput,
  recIdToFilePath: Map<string, string>,
): Stage3a1ResponseBody {
  return {
    batch_index: raw.batch_index,
    total_batches: raw.total_batches,
    _stage_flags: raw._stage_flags,
    recommendations: raw.recommendations.map((rec) => ({
      ...rec,
      source_file_path: recIdToFilePath.get(rec.recommendation_id) ?? "",
      owner_name: null,
      action_items: rec.action_items.map((ai) => ({
        ...ai,
        owner_name: null,
        parent_action_item_id: null,
        is_derivative_reminder: false,
        source_plan_id: null,
      })),
    })),
  };
}

