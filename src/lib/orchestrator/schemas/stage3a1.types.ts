const ActionItemSchema = z
  .object({
    action_item_id: z.string().min(1),
    description: z.string().min(1),
    sub_steps: z.array(z.string()),
    category: RecommendationCategoryEnum,
    source_recommendation_id: z.string().regex(/^REC-[A-Z]{3}-\d{3}$/),
    source_phase_or_step: z.string(),
    owner: ActionOwnerEnum,
    owner_name: z.string().nullable(),
    timing_bucket: TimingBucketEnum,
    depends_on: z.array(z.string()),
    is_decision_needed: z.boolean(),

    // Lifecycle additions populated by Stage 3a.1.
    duration_class: DurationClassEnum,
    check_in_cadence: CheckInCadenceEnum.nullable(),
    partner_required: z.boolean(),
    partner_type: PartnerTypeEnum.nullable(),
    parent_action_item_id: z.string().nullable(),
    is_derivative_reminder: z.boolean(),
    source_plan_id: z.string().nullable(),
    auto_generated_reminder_template: AutoGeneratedReminderTemplateSchema.nullable(),
  })
  .superRefine((ai, ctx) => {
    // long_running ⇔ check_in_cadence !== null ⇔ auto_generated_reminder_template !== null
    const longRunning = ai.duration_class === "long_running";
    const cadencePresent = ai.check_in_cadence !== null;
    const templatePresent = ai.auto_generated_reminder_template !== null;
    if (longRunning !== cadencePresent || longRunning !== templatePresent) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: duration_class === 'long_running' ⇔ check_in_cadence !== null ⇔ auto_generated_reminder_template !== null. All three must agree.",
      });
    }

    // partner_required ⇔ partner_type !== null
    if (ai.partner_required && ai.partner_type === null) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: partner_required === true requires partner_type !== null.",
      });
    }
    if (!ai.partner_required && ai.partner_type !== null) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: partner_required === false requires partner_type === null.",
      });
    }

    // Stage 3a.1 never sets these.
    if (ai.is_derivative_reminder) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: is_derivative_reminder must be false at Stage 3a.1 emit time.",
      });
    }
    if (ai.parent_action_item_id !== null) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: parent_action_item_id must be null at Stage 3a.1 emit time.",
      });
    }
    if (ai.source_plan_id !== null) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "ActionItem invariant: source_plan_id must be null at Stage 3a.1 emit time.",
      });
    }

    // Cadence-to-trigger_threshold_days deterministic mapping.
    if (ai.auto_generated_reminder_template !== null && ai.check_in_cadence !== null) {
      const expected: Record<string, number> = {
        weekly: 7,
        biweekly: 14,
        monthly: 30,
        quarterly: 90,
        annually: 365,
      };
      const want = expected[ai.check_in_cadence];
      if (
        want !== undefined &&
        ai.auto_generated_reminder_template.trigger_threshold_days !== want
      ) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message: `ActionItem invariant: auto_generated_reminder_template.trigger_threshold_days must be ${want} for cadence '${ai.check_in_cadence}', got ${ai.auto_generated_reminder_template.trigger_threshold_days}.`,
        });
      }
      if (ai.auto_generated_reminder_template.cadence !== ai.check_in_cadence) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message: `ActionItem invariant: auto_generated_reminder_template.cadence must equal check_in_cadence ('${ai.check_in_cadence}'), got '${ai.auto_generated_reminder_template.cadence}'.`,
        });
      }
    }
  });

const QuantifiedImpactSchema = z
  .object({
    estimate: NumericValueSchema.nullable(),
    formula_id: z.string().nullable(),
    formula_source_file: z.string().nullable(),
    computation_inputs: z.record(z.string(), z.unknown()),
    pending_reconciliation: z.boolean(),
    alternative_values: z.array(
      z.object({
        value: NumericValueSchema,
        formula_variant: z.string(),
        awaiting: z.string(),
        context: z.string(),
      }),
    ),
    qualitative_phrasing: z.string().nullable(),
    reason_no_formula: z.string().nullable(),
    blocked_inputs: z.array(
      z.object({
        input_name: z.string(),
        blocked_reason: z.string(),
        source: z.string(),
        would_unblock_when: z.string(),
      }),
    ),
  })
  .superRefine((qi, ctx) => {
    // State A: estimate !== null ⇒ no blocked_inputs, no alternative_values, no qualitative_phrasing
    if (qi.estimate !== null) {
      if (qi.blocked_inputs.length > 0) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State A invariant: estimate !== null requires blocked_inputs to be empty.",
        });
      }
      if (qi.alternative_values.length > 0) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State A invariant: estimate !== null requires alternative_values to be empty.",
        });
      }
      if (qi.qualitative_phrasing !== null) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State A invariant: estimate !== null requires qualitative_phrasing to be null.",
        });
      }
    }

    // State C: alternative_values.length > 0 ⇔ pending_reconciliation === true
    if (qi.alternative_values.length > 0 && !qi.pending_reconciliation) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "QuantifiedImpact State C invariant: alternative_values.length > 0 requires pending_reconciliation === true.",
      });
    }
    if (qi.pending_reconciliation && qi.alternative_values.length === 0) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "QuantifiedImpact State C invariant: pending_reconciliation === true requires alternative_values to be non-empty.",
      });
    }

    // State B: blocked_inputs.length > 0 ⇒ estimate === null
    if (qi.blocked_inputs.length > 0 && qi.estimate !== null) {
      ctx.addIssue({
        code: z.ZodIssueCode.custom,
        message:
          "QuantifiedImpact State B invariant: blocked_inputs.length > 0 requires estimate === null.",
      });
    }

    // State D: reason_no_formula !== null ⇒ estimate === null AND formula_id === null AND qualitative_phrasing !== null
    if (qi.reason_no_formula !== null) {
      if (qi.estimate !== null) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State D invariant: reason_no_formula !== null requires estimate === null.",
        });
      }
      if (qi.formula_id !== null) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State D invariant: reason_no_formula !== null requires formula_id === null.",
        });
      }
      if (qi.qualitative_phrasing === null) {
        ctx.addIssue({
          code: z.ZodIssueCode.custom,
          message:
            "QuantifiedImpact State D invariant: reason_no_formula !== null requires qualitative_phrasing !== null.",
        });
      }
    }
  });

