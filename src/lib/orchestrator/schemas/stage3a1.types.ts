// Stage 3a.1 — Batch Quantifier output schema.
//
// Stage 3a.1 takes a batch of 15–25 SelectedRecommendation entries and emits
// SequencedRecommendation[] with quantified_impact + action_items per rec.
// The container types it consumes/emits live in pipelineTypes.ts; this file
// owns only the per-batch wrapper types and the zod shape that validates the
// LLM's response before it leaves the stage.
//
// Cross-batch validation (orphan refs, coverage gaps, flag union) is Stage
// 3a.2's job; this schema validates ONLY per-batch invariants.

